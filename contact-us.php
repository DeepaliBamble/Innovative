    <script>
        document.getElementById('contactForm').addEventListener('submit', async function(e) {
            e.preventDefault();

            const submitBtn = document.getElementById('contactSubmitBtn');
            const btnText = document.getElementById('contactBtnText');
            const messageDiv = document.getElementById('contactMessage');

            // Disable button and show loading
            submitBtn.disabled = true;
            btnText.textContent = 'SENDING...';
            messageDiv.style.display = 'none';

            const formData = new FormData(this);

            try {
                const response = await fetch('ajax/submit-contact.php', {
                    method: 'POST',
                    body: formData
                });

                const result = await response.json();

                if (result.success) {
                    messageDiv.className = 'alert alert-success mb-3';
                    messageDiv.innerHTML = '<i class="fa-solid fa-circle-check me-2"></i>' + result.message;
                    messageDiv.style.display = 'block';
                    this.reset();

                    // Scroll to message
                    messageDiv.scrollIntoView({ behavior: 'smooth', block: 'center' });
                } else {
                    messageDiv.className = 'alert alert-danger mb-3';
                    messageDiv.innerHTML = '<i class="fa-solid fa-circle-exclamation me-2"></i>' + result.message;
                    messageDiv.style.display = 'block';
                }
            } catch (error) {
                messageDiv.className = 'alert alert-danger mb-3';
                messageDiv.innerHTML = '<i class="fa-solid fa-circle-exclamation me-2"></i>An error occurred. Please try again.';
                messageDiv.style.display = 'block';
            } finally {
                submitBtn.disabled = false;
                btnText.textContent = 'SEND MESSAGE';
            }
        });
    </script>
